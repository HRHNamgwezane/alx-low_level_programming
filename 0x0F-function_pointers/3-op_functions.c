#include "3-calc.h"

/**
 * op_add - returns sum of two numbers
 * @a: number to be added
 * @b: number to be added
 * Return: sum of a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of two numbers
 * @a: number to be subtracted
 * @: number be subtracted
 * Return: the difference between a & h
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of two numbers
 * @a: number to multiply
 * @b: number to multiply
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the division
 * @a: number to divide
 * @b: number to divide
 * Return: division result of a & b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of division of a by b
 * @a: number to divide
 * @b number to dividion
 * Return: remainder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
